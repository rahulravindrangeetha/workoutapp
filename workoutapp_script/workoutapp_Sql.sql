DROP DATABASE IF EXISTS workoutapp;
CREATE DATABASE workoutapp;

USE workoutapp;

CREATE TABLE WORKOUT_CATEGORY(CATEGORY_ID INT NOT NULL,
CATEGORY_NAME VARCHAR(64),
PRIMARY KEY(CATEGORY_ID));


CREATE TABLE WORKOUT_COLLECTION(WORKOUT_ID INT NOT NULL,
WORKOUT_TITLE VARCHAR(128) ,
WORKOUT_NOTE VARCHAR(256) ,
calories_burn_per_min DOUBLE ,
CATEGORY_ID INT ,
FOREIGN KEY(CATEGORY_ID) REFERENCES WORKOUT_CATEGORY(CATEGORY_ID),
PRIMARY KEY(WORKOUT_ID));


CREATE TABLE WORKOUT_ACTIVE(WORKOUT_ID INT,
COMMENT VARCHAR(64) ,
STATUS VARCHAR(10) ,
START_DATE DATE ,
END_DATE DATE ,
START_TIME TIME ,
END_TIME TIME,
FOREIGN KEY(WORKOUT_ID) REFERENCES WORKOUT_COLLECTION(WORKOUT_ID) );



